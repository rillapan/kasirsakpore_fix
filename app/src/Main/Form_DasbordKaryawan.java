    private void notifikasi() {
        try {
            String sql = "SELECT COUNT(*) AS kurang FROM view_stok_rendah";
            ps = Koneksi.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next() && rs.getInt("kurang") > 0) {
                p_notifikasi.setVisible(true);
            } else {
                p_notifikasi.setVisible(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error notifikasi(): " + e.getMessage());
        }
    }

    private void tampilStok() {
        try {
            DefaultTableModel model = new DefaultTableModel(new String[]{"Kode Produk", "Nama Produk", "Stok"}, 0);
            String sql = "SELECT * FROM view_stok_rendah";
            ps = Koneksi.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("kode_produk"),
                    rs.getString("nama_produk"),
                    rs.getInt("stok_produk")
                });
            }
            tb_stok.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error tampilStok(): " + e.getMessage());
        }
    }

