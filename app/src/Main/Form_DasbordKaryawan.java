private void totalPenjualan() {
        try {
            String sql = "SELECT total_penjualan FROM view_total_penjualan WHERE tanggal_penjualan = CURRENT_DATE";
            ps = Koneksi.getConnection().prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()) {
                lb_totalPenjualan.setText(String.valueOf(rs.getInt("total_penjualan")));
            } else {
                lb_totalPenjualan.setText("0");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error totalPenjualan(): " + e.getMessage());
        }
    }



